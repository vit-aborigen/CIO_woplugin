def recursive(i, j, pyramid):
    if i == len(pyramid) - 1:
        return pyramid[i][j]
    left = recursive(i + 1, j, pyramid)
    right = recursive(i + 1, j + 1, pyramid)
    if left >= right:
        return left + pyramid[i][j]
    return right + pyramid[i][j]

def count_gold(pyramid):
    return recursive(0,0,pyramid)


