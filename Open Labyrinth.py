from collections import deque, defaultdict

def checkio(maze_map):
    directions = {(1, 0): 'N', (-1, 0): 'S', (0, 1): 'W', (0, -1): 'E'}
    h, w = len(maze_map), len(maze_map[0])
    frontier = deque([(1, 1)])
    visited = defaultdict()
    visited[(1,1)] = None

    while frontier:
        current = frontier.popleft()
        if current == maze_map[-2][-2]:
            break
        ci, cj = current
        for di, dj  in directions:
            i, j = ci + di, cj + dj
            if (0 <= i < h) and (0 <= j < w) and (maze_map[i][j] == 0) and ((i, j) not in visited):
                visited[(i, j)] = current
                frontier.append((i, j))
    path = show_path((1,1), (h-2, w-2), visited)
    char_path = [tuple(map(lambda i,j: i-j, i, j)) for i,j in zip(path, path[1:])]
    result = ''.join([directions[step] for step in char_path])
    return result

def show_path(start, goal, visited):
    path = []
    current = goal
    while current != start:
        current = visited[current]
        path.append(current)
    path.reverse()
    path.append(goal)
    return path


